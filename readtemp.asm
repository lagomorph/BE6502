; 65C22 pin2: data
; 65C22 pin3: clock
; 65C22 pin4: errorLED (anode)
; 65C22 pin5: downLED (anode)
; 65C22 pin6: upLED (anode)

; circuit notes:
; 4.7kΩ weak pullup on data line to make sure we're seeing DS1624 pulling down line on ACKs
; This may be unnecessary but it's not clear from the datasheet if this is left floating or not.
; 820Ω resistor (any reasonable value) between data line on DS1624 and 65C22 just for extra safety
; LEDs with anodes on pins 4, 5, 6 and cathodes to ground via appropriate dropping resistors
; The Dallas and Maxim datasheets are inconsistent as to whether we get 4 or 5 bits of
; fractional temp. We're getting 5 with this particular chip (Dallas).
; In continuous conversion mode the done flag is never set. This isn't in the datasheets but
; found in a Dallas app note. We're waiting over a second anyway so it should be done.

PORTB = $6000
PORTA = $6001
DDRB = $6002
DDRA = $6003
T1CL = $6004
T1CH = $6005
ACR = $600b
IFR = $600d
IER = $600e

E  = %10000000
RW = %01000000
RS = %00100000

CONTROLW = %10010000
CONTROLR = %10010001
MEMORY = $17
CONFIG = $ac
STARTCONVERT = $ee
READTEMP = $aa

intCount = $00
currTemp = $01
lastTemp = $03
lowTemp = $05
highTemp = $07
message = $09
displayTempVal = $0b

* = $8000

start:
  lda #%11111111  ; set all pins on port B to output
  sta DDRB
                  ; set all pins on port A to output, top 3 pins (E, RW, RS),
                  ; upLED, downLED, bottom three pins (errorLED, DS1624 clock, data)
  sta DDRA

  ldx #$ff
  txs

  ldx #$80
  stx lastTemp    ; out of range value that indicates "none"

  lda PORTA
  and #%11100001  ; upLED, downLED, errorLED, clock low
  sta PORTA

  lda #%00111000 ; set 8-bit mode; 2-line display; 5x8 font
  jsr lcdCommand
  lda #%00001110 ; display on; cursor on; blink off
  jsr lcdCommand
  lda #%00000110 ; increment and shift cursor; don't shift display
  jsr lcdCommand
  lda #%00000001 ; clear display
  jsr lcdCommand

  lda #$00
  sta intCount
  sta lowTemp+1
  sta highTemp+1
  lda #$7d
  sta lowTemp
  lda #$c9
  sta highTemp

  ; send start convert
  jsr sendStart
  lda #CONTROLW
  jsr sendByte
  lda #STARTCONVERT
  jsr sendByte
  jsr sendStop

  ; set up interrupt
  lda ACR
  ora #%01000000  ; timer 1 continuous, PB7 changing disabled
  and #%01111111
  sta ACR
  lda #$ff        ; max time
  sta T1CL
  sta T1CH        ; starts timer
  lda #%11000000  ; enable timer 1 interrupt
  sta IER

; 1) temp measurement and line 1 display when intCount % 16 == 0
; 2) line 2 display when intCount % 16 == 1
; display low nybble of intCount every int
; there is easily enough time to do 1 and 2 at once though
; 1 takes about 5ms, 2 takes about 2.5ms, and there are about 65.5ms between ints
; other 14 of 16 interrupts just to show the interrupt counter takes about .28ms
loop:
  wai
  lda T1CL  ; 4, resets interrupt flag
  inc intCount
  lda intCount
  and #$0f
  bne +
  jsr readTemp
  jsr validateTemp
  jsr updateHighLowTemps
  jsr updateUpDownLEDs
  jsr updateDisplayLine1
  bra ++
+ cmp #$01
  bne ++
  jsr updateDisplayLine2
++lda #%10001111  ; go to last col on 1st line
  jsr lcdCommand
  lda intCount
  and #$0f
  jsr hexDigit
  jsr displayByte
  bra loop

readTemp:
  jsr sendStart
  lda #CONTROLW
  jsr sendByte
  lda #READTEMP
  jsr sendByte
  jsr sendStart
  lda #CONTROLR
  jsr sendByte
  lda #$00        ; ACK
  jsr receiveByte
  sta currTemp
  lda #$01        ; NAK
  jsr receiveByte
  sta currTemp+1
  jsr sendStop
  rts

validateTemp:
  lda currTemp+1
  and #$07
  beq +
  jsr updateDisplayLine1
  lda #%11000000  ; go to 2nd line
  jsr lcdCommand
  lda #<.errorText
  sta message
  lda #>.errorText
  sta message+1
  jsr displayString
  jmp error
+ rts

updateHighLowTemps:
  lda highTemp+1  ; highTemp-currTemp
  cmp currTemp+1
  lda highTemp
  sbc currTemp
  bvc +  ; N eor V
  eor #$80
+ bpl +
  lda currTemp
  sta highTemp
  lda currTemp+1
  sta highTemp+1

+ lda currTemp+1  ; currTemp-lowTemp
  cmp lowTemp+1
  lda currTemp
  sbc lowTemp
  bvc +  ; N eor V
  eor #$80
+ bpl +
  lda currTemp
  sta lowTemp
  lda currTemp+1
  sta lowTemp+1
+ rts

updateUpDownLEDs:
  lda lastTemp
  cmp #$80
  beq ++  ; no lastTemp
  lda lastTemp+1  ; lastTemp-currTemp
  cmp currTemp+1
  lda lastTemp
  sbc currTemp
  bvc +  ; N eor V
  eor #$80
+ bpl +  ; lastTemp >= currTemp
  ; up
  lda PORTA
  ora #%00010000
  and #%11110111
  sta PORTA
  bra ++
+ lda lastTemp
  cmp currTemp
  bne +
  lda lastTemp+1
  cmp currTemp+1
  beq ++
  ; down
+ lda PORTA
  ora #%00001000
  and #%11101111
  sta PORTA
; end
++lda currTemp
  sta lastTemp
  lda currTemp+1
  sta lastTemp+1
  rts

updateDisplayLine1:
  lda #%00000010  ; home display
  jsr lcdCommand
  lda currTemp
  jsr displayHex
  lda currTemp+1
  jsr displayHex
  lda #' '
  jsr displayByte

  lda currTemp
  sta displayTempVal
  lda currTemp+1
  sta displayTempVal+1
  jsr displayTemp
  rts

updateDisplayLine2:
  lda #%11000000  ; go to 2nd line
  jsr lcdCommand
  lda intCount
  and #$20
  bne +
  lda #<.minText
  sta message
  lda #>.minText
  sta message+1
  jsr displayString

  lda lowTemp
  sta displayTempVal
  lda lowTemp+1
  bra ++
+ lda #<.maxText
  sta message
  lda #>.maxText
  sta message+1
  jsr displayString

  lda highTemp
  sta displayTempVal
  lda highTemp+1
++sta displayTempVal+1
  jsr displayTemp
  rts

displayHex:
  phx
  tax
  lsr
  lsr
  lsr
  lsr
  jsr hexDigit
  jsr displayByte
  txa
  and #$0f
  jsr hexDigit
  jsr displayByte
  plx
  rts

hexDigit:
  cmp #$0a
  bcs +
  adc #'0'
  rts
+ adc #('a'-$0b)
  rts

displayTemp:
  phx
  phy
  lda displayTempVal
  bmi +
  clc
  bra ++
+ sec
++ror displayTempVal
  ror displayTempVal+1
  lda displayTempVal
  bpl +
  lda displayTempVal+1
  eor #$ff
  adc #$01
  sta displayTempVal+1
  lda displayTempVal
  eor #$ff
  adc #$00
  sta displayTempVal
  lda displayTempVal+1
  adc #<negativeTempTab
  tax  ; need to swap displayTempVal, displayTempVal+1
  lda displayTempVal
  adc #>negativeTempTab
  sta displayTempVal+1
  stx displayTempVal
  bra ++
+ lda displayTempVal+1
  adc #<tempTab
  tax  ; need to swap displayTempVal, displayTempVal+1
  lda displayTempVal
  adc #>tempTab
  sta displayTempVal+1
  stx displayTempVal

++ldy #$00  ; display what's at displayTempVal
  lda (displayTempVal),y
  cmp #$f0
  tax
  bcs +  ; negative
  lsr
  lsr
  lsr
  lsr
  jsr hexDigit
  bra ++
+ lda #'-'
++jsr displayByte
  txa
  and #$0f
  jsr hexDigit
  jsr displayByte
  iny

  lda (displayTempVal),y
  tax
  lsr
  lsr
  lsr
  lsr
  jsr hexDigit
  jsr displayByte
  lda #'.'
  jsr displayByte
  txa
  and #$0f
  jsr hexDigit
  jsr displayByte
  iny

  lda (displayTempVal),y
  jsr displayHex
  iny

  lda (displayTempVal),y
  jsr displayHex
  ply
  plx
  rts

error:
  lda PORTA
  ora #%00000100  ; errorLED on
  sta PORTA
  jsr sendStop
  lda #%01000000  ; disable timer 1 interrupt
  sta IER
- wai
  bra -  ; just in case

sendStart:
  lda PORTA
  ora #%00000001  ; data high
  sta PORTA
  ora #%00000010  ; clock high
  sta PORTA
  and #%11111110  ; data low
  sta PORTA
  and #%11111101  ; clock low
  sta PORTA
  rts

sendStop:
  lda PORTA
  and #%11111110  ; data low
  sta PORTA
  ora #%00000010  ; clock high
  sta PORTA
  ora #%00000001  ; data high
  sta PORTA
  rts

sendByte:
  phx
  phy
  ldx #%11111111  ; data is an output
  stx DDRA
  ldx PORTA
  ldy #$08
_sendBit:
  asl
  pha
  txa
  bcs _sendOneBit
  and #%11111110
  bra +
_sendOneBit:
  ora #%00000001
+ sta PORTA
  ora #%00000010  ; clock high
  sta PORTA
  and #%11111101  ; clock low
  sta PORTA
  pla
  dey
  bne _sendBit
  ply
  plx
  jsr checkAck
  bcs error
  rts

receiveByte:
  phx
  phy
  pha
  lda #%11111110  ; data is an input
  sta DDRA
  ldy #$08
  lda PORTA
_receiveBit:
  ora #%00000010  ; clock high
  sta PORTA
  lda PORTA
  ror
  txa
  rol
  tax
  lda PORTA
  and #%11111101  ; clock low
  sta PORTA
  dey
  bne _receiveBit
  pla
  jsr sendAckNak
  txa
  ply
  plx
  rts

checkAck:
  lda #%11111110  ; data is an input
  sta DDRA
  lda PORTA
  ora #%00000010  ; clock high
  sta PORTA
  lda PORTA       ; read ack bit
  and #%11111101  ; clock low
  sta PORTA
  ror             ; ack bit returned in carry
  lda #%11111111  ; data is an output
  sta DDRA
  rts

sendAckNak:
  beq _sendAck
  lda #%11111111  ; data is an output
  sta DDRA
  lda PORTA
  ora #%00000001  ; data high
  bra +
_sendAck:
  lda #%11111111  ; data is an output
  sta DDRA
  lda PORTA
  and #%11111110  ; data low
+ sta PORTA
  ora #%00000010  ; clock high
  sta PORTA
  and #%11111101  ; clock low
  sta PORTA
  rts

lcdCommand:
  jsr lcdWait
  sta PORTB
  lda PORTA
  and #<!(RS | RW | E)
  sta PORTA
  ora #E
  sta PORTA
  and #<!E
  sta PORTA
  rts

displayByte:
  jsr lcdWait
  sta PORTB
  lda PORTA
  ora #RS
  and #<!(RW | E)
  sta PORTA
  ora #E
  sta PORTA
  and #<!E
  sta PORTA
  rts

lcdWait:
  pha
  phx
  lda #%00000000  ; set all pins on port B to input
  sta DDRB
  lda PORTA
  ora #RW
  and #<!(RS | E)
  sta PORTA
- lda PORTA
  ora #E
  sta PORTA
  ldx PORTB
  and #<!E
  sta PORTA
  txa
  bmi -
  lda #%11111111  ; set all pins on port B to output
  sta DDRB
  plx
  pla
  rts

displayString:
  phy
  ldy #0
- lda (message),y
  beq +
  jsr displayByte
  iny
  bra -
+ ply
  rts

nmi:
  rti

irq:
  rti

